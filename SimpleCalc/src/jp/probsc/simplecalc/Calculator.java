package jp.probsc.simplecalc;

/**
 * 計算クラス
 */
public class Calculator
{
	/** 足し算 */
	public static int Add(int a, int b)
	{
		return a + b;
	}
	
	/** 引き算 */
	public static int Sub(int a, int b)
	{
		return a - b;
	}
	
	/** 掛け算 */
	public static int Mul(int a, int b)
	{
		return a * b;
	}
	
	/** 割り算 */
	public static double Div(int a, int b)
	{
		return a / b;
	}
}
